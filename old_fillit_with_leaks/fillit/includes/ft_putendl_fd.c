#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	char end_line;

	if (s)
	{
		end_line = '\n';
		while (*s != 0)
		{
			write(fd, s, 1);
			s++;
		}
		write(fd, &end_line, 1);
	}
}
