#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char		*output;
	char		*str;
	char const	*src;

	if (s)
	{
		str = ft_strnew(len);
		output = str;
		src = &s[start];
		if (str)
		{
			while (*src != 0 && len > 0)
			{
				*str++ = *src++;
				len--;
			}
			return (output);
		}
		else
		{
			return (NULL);
		}
	}
	else
		return (NULL);
}
