#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char *x;
	char *y;

	x = (char*)src;
	y = (char*)dst;
	while (n--)
	{
		*y++ = *x++;
	}
	return (dst);
}
