#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t len)
{
	char *finaly;

	finaly = dest;
	while (*src != 0 && len > 0)
	{
		*dest++ = *src++;
		len--;
	}
	while (len > 0)
	{
		*dest++ = 0;
		len--;
	}
	return (finaly);
}
